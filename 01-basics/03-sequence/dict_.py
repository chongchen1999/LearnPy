# Creating and modifying a dictionary
person = {"name": "Alice", "age": 25, "city": "New York"}
person["age"] = 26  # Update value
person["profession"] = "Engineer"  # Add new key-value pair
print("Dictionary:", person)  # Output: {'name': 'Alice', 'age': 26, 'city': 'New York', 'profession': 'Engineer'}
